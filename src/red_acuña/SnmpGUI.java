    private void jbtnEscanearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEscanearActionPerformed
		try {
			// TODO add your handling code here:
			this.hosts = new checkHosts(this.jtfNet.getText(),Integer.parseInt(this.jtaTO.getText()));
			this.hosts.checkHosts();
			String aux="";
			for(int i=0;i<this.hosts.getEquiposRed().size();i++){
				aux+="IP: "+this.hosts.getEquiposRed().get(i)+"\n";
				this.jcbIPS.addItem(this.hosts.getEquiposRed().get(i));
			}
			this.jtaIPs.setText(aux);
			
		} catch (IOException ex) {
			Logger.getLogger(SnmpGUI.class.getName()).log(Level.SEVERE, null, ex);
		}
    }//GEN-LAST:event_jbtnEscanearActionPerformed

